class Gallery extends Component{
  state = {activeImage: 0}


  changeImage = (id) => {
    const {activeImage} = this.state
    this.setState({activeImage: id})
  }

  render(){
    const {activeImage} = this.state
    const displayImageUrl = imagesList[activeImage].imageUrl
    const imageAltText = imagesList[activeImage].imageAltText

    return(
      <div className="bg-container">
          <div className="content-container">
            <img alt={imageAltText} className="image" src={displayImageUrl}/>
            <h1 className="title"> Nature Photography</h1>
            <p className="description"> Nature Photography by Rahul</p>
            <ul className="thumnail-list">
              {imagesList.map(eachImage => <ThumbnailItem isActive={activeImage} toChangeImage={this.changeImage} key={eachImage.id} details={eachImage} />)}
            </ul>
        </div>
      </div>
    )
  }
}

export default Gallery;